TEST(rotate_matrix, Negative) {
  int a[] = {1,2,3,4,5};
  int a_1[] = {2,3,4,5,1};
  int a_2[] = {3,4,5,1, 2};
  int i;

  rotate_matrix(a,1, sizeof(a)/sizeof(a[0]));
  for(i = 0; i < 5; i++)
  {
    EXPECT_TRUE(a[i] == a_1[i]);
  }
  
  rotate_matrix(a,1, sizeof(a)/sizeof(a[0]));
  for(i = 0; i < 5; i++)
  {
    EXPECT_TRUE(a[i] == a_2[i]);
  }
}

